
import streamlit as st
from PIL import Image

st.set_page_config(page_title="نور و سایه", layout="centered")

st.title("🌗 منطق نور و سایه")
st.markdown("---")

# توضیح اولیه
st.markdown("**به دنیای منطق نور و سایه خوش آمدید.**")
st.write("در اینجا می‌خواهیم مفهومی را بررسی کنیم که بر اساس آن، هر آنچه در جهان قابل مشاهده است، به صورت نور نمایان می‌شود، و آنچه که دیده نمی‌شود، در تاریکی یا سایه باقی می‌ماند.")

# تصویر شکستن نور
st.subheader("🔍 مشاهده منطق نور و سایه")
if st.button("تجزیه نور توسط منشور"):
    st.write("این تصویر نشان می‌دهد چگونه نور سفید هنگام عبور از منشور به رنگ‌های مختلف تجزیه می‌شود.")
    try:
        image = Image.open("assets/prism.jpg")
        st.image(image, caption="نور سفید و تجزیه آن", use_container_width=True)
    except:
        st.warning("تصویر موجود نیست. لطفاً تصویر 'prism.jpg' را در پوشه assets قرار دهید.")

# پیام پایانی
st.markdown("---")
st.info("نسخه ابتدایی این برنامه توسط فرجان احسانی طراحی شده است. ایده‌ها و بازخوردهای شما می‌تواند در توسعه نسخه‌های آینده بسیار مؤثر باشد.")
